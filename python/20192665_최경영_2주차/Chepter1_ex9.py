import turtle
t = turtle.Turtle()
t.shape("turtle")


t.up()
t.goto(-170,0)
t.down()
t.circle(100)

t.up()
t.goto(0,0)
t.down()
t.circle(100)

t.up()
t.goto(170,0)
t.down()
t.circle(100)

t.up()
t.goto(-85,-100)
t.down()
t.circle(100)

t.up()
t.goto(85,-100)
t.down()
t.circle(100)

turtle.mainloop()